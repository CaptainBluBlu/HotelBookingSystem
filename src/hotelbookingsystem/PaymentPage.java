    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String input = txtPayed.getText();
        double userPay;
        try {
            userPay = Double.valueOf(input);
            if ((userPay - finalPrice) >= 0) {
                double change = userPay - finalPrice;
                this.setVisible(false);        
                if (fromBooking) {
                    JOptionPane.showMessageDialog(null, "Successfully paid! And added booking");
                    bookingPayment.Pay(userPay);
                    bookingPayment.writeBooking();
                    Invoice paymentInvoice = new Invoice(bookingPayment);
                    paymentInvoice.setVisible(true);
                } else {
                    JOptionPane.showMessageDialog(null, "Successfully paid!");
                    this.payment.Pay(this.rowIndex, userPay, change);
                    Invoice paymentInvoice = new Invoice(payment, roomNo, customerName, "paid");
                    paymentInvoice.setVisible(true);
                }
                                  
            } else {
                JOptionPane.showMessageDialog(null, "Please make sure customer pay the right amount.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(rootPane, e, "Invalid Input Type", HEIGHT);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(PaymentPage.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

