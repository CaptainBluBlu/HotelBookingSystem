    private void formWindowClosed(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosed
        if (fromBooking) {
            boolean writeStatus = bookingPayment.writeBooking(); // write unpaid booking if user decide to not pay and close the form
            if (writeStatus) {
                JOptionPane.showMessageDialog(null, "Booking has been added. But payment still pending.");
            } else {
                JOptionPane.showMessageDialog(null, "There was an error writing booking.");
            }            
        }
    }//GEN-LAST:event_formWindowClosed

